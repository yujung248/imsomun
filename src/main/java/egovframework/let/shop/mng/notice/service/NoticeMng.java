package egovframework.let.shop.mng.notice.service;

public class NoticeMng {
	private int n_idx;
	private int n_div;
	private String n_sub;
	private String n_content;
	private String n_date;
	private String a_id;
	
	public int getN_idx() {
		return n_idx;
	}
	public void setN_idx(int n_idx) {
		this.n_idx = n_idx;
	}
	public int getN_div() {
		return n_div;
	}
	public void setN_div(int n_div) {
		this.n_div = n_div;
	}
	public String getN_sub() {
		return n_sub;
	}
	public void setN_sub(String n_sub) {
		this.n_sub = n_sub;
	}
	public String getN_content() {
		return n_content;
	}
	public void setN_content(String n_content) {
		this.n_content = n_content;
	}
	public String getN_date() {
		return n_date;
	}
	public void setN_date(String n_date) {
		this.n_date = n_date;
	}
	public String getA_id() {
		return a_id;
	}
	public void setA_id(String a_id) {
		this.a_id = a_id;
	}

}
